enum GameActions {
  GAME_OVER, // The game is over
  HIDE_ABOUT_DIALOG, // Hide about dialog
  MOVE_DOWN, // Move tetrimino down
  MOVE_LEFT, // Move tetrimino left
  MOVE_RIGHT, // Move tetrimino right
  MUTE_MUSIC, // Mute music
  PAUSE, // Pause the game
  RESTART, // Restart Game
  RESUME, // Resume a paused game
  ROTATE_CLOCKWISE, // Rotate tetrimino
  SHOW_ABOUT_DIALOG, // Show about dialog
  UNMUTE_MUSIC, // Unmute music
}

export { GameActions };
